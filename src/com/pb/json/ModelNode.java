package com.pb.json;

public class ModelNode {
    private Integer id;
    private String name;
 	private String type;
 	private Integer haveA;
 	private Integer haveB;
 	
 	private ModelTree subjects;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getHaveA() {
		return haveA;
	}

	public void setHaveA(Integer haveA) {
		this.haveA = haveA;
	}

	public Integer getHaveB() {
		return haveB;
	}

	public void setHaveB(Integer haveB) {
		this.haveB = haveB;
	}

	public ModelTree getSubjects() {
		return subjects;
	}

	public void setSubjects(ModelTree subjects) {
		this.subjects = subjects;
	}
 	
 	
	
}
