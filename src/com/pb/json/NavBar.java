package com.pb.json;

import java.util.List;

public class NavBar {
	private Integer institution_id;
	private String name;
	private int isMain; //1表示是主单位，0表示子单位
	
	private List<NavBar> children;

	public Integer getInstitution_id() {
		return institution_id;
	}

	public void setInstitution_id(Integer institution_id) {
		this.institution_id = institution_id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIsMain() {
		return isMain;
	}

	public void setIsMain(int isMain) {
		this.isMain = isMain;
	}

	public List<NavBar> getChildren() {
		return children;
	}

	public void setChildren(List<NavBar> children) {
		this.children = children;
	}
	

}
